package com.monocept.oopsmodel;

public class Student {
	private int rollno;
	private String name;
	private int age;
	
	public void setRollnumber(int rollno1) {
		rollno=rollno1;
	}
	public int getRollnumber() {
		return rollno;
	}
	public void setName(String name1) {
		name=name1;
	}
	public String getName(){
		return name;
	}
	public void setAge(int age1) {
		age=age1;
	}
	public int getAge() {
		return age;
	}

}
